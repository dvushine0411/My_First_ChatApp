import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import { Card, CardContent } from "@/components/ui/card"
import { Input } from "@/components/ui/input"
import {z} from 'zod';
import {useForm} from 'react-hook-form';
import {zodResolver} from '@hookform/resolvers/zod';
import { PasswordInput } from "@/components/ui/PasswordInput";
import { useAuthStore } from "@/stores/useAuthStore";
import { useNavigate } from "react-router";


// Dùng thư viện zod để kiểm tra username và password điền vào //

const SignInSchema = z.object({
  username: z.string().min(3, 'Tên đăng nhập phải có ít nhất 3 ký tự'),
  password: z.string().min(6, 'Mật khẩu phải có ít nhất 6 ký tự')

});

type SigninFormValues = z.infer<typeof SignInSchema>

export function SigninForm({ className, ...props }: React.ComponentProps<"div">) {

    const { signIn } = useAuthStore();
    const navigate = useNavigate();

    const {register, handleSubmit, formState: {errors, isSubmitting}} = useForm<SigninFormValues>({
        resolver: zodResolver(SignInSchema)
      })
    
      const onSubmit = async (data: SigninFormValues) => {
        // Gọi backend để signup //

        const {username, password} = data;
        await signIn(username, password);

        navigate("/");

      }
      
      return (
    <div className={cn("flex flex-col gap-6", className)} {...props}>
      <Card className="overflow-hidden p-0 border-border">
        <CardContent className="grid p-0 md:grid-cols-2">
          <form className="p-6 md:p-8" onSubmit={handleSubmit(onSubmit)}>      
            <div className="flex flex-col gap-6">
              {
                // Header và logo
                <div className="flex flex-col items-center text-center gap-2">
                  <a href="/"
                     className="mx-auto block w-fit text-center"
                  >
                    <img 
                      src="/vite.svg" 
                      alt="logo" 
                    
                    />
                  </a>

                  <h1 className="text-2xl font-bold">Chào mừng quay lại</h1>
                  <p className="text-muted-foreground text-balance">
                    Đăng nhập vào tài khoản VIT Chat của bạn
                  </p>
                </div>
              }


              {
                // username //
                <div className="flex flex-col gap3">
                  <label
                    htmlFor="username"
                    className="block text-sm"
                  >
                    Tên đăng nhập
                  </label>

                  <Input
                    type="text"
                    id="username"
                    {...register("username")}
                  />

                  {errors.username && (
                      <p className="text-destructive text-sm">
                        {errors.username.message}
                      </p>
                    )}
                </div>
              }


              {
                // Password //
                <div className="flex flex-col gap3">
                  <label
                    htmlFor="password"
                    className="block text-sm"
                  >
                    Mật khẩu
                  </label>

                  <PasswordInput
                    id="password"
                    {...register("password")}
                  />

                  {errors.password && (
                      <p className="text-destructive text-sm">
                        {errors.password.message}
                      </p>
                    )}
                </div>
              }

              {
                // Nút đăng nhập //
                <Button
                  type="submit"
                  className="w-full"  
                  disabled={isSubmitting}   // Button sẽ bị vô hiệu hoá trong khi đang gửi đi (isSubmitting) để tránh spam gửi nhiều lần //
                >
                  Đăng nhập
                </Button>
              }
                <div className="text-center text-sm">
                  Chưa có tài khoản? {" "}
                  <a
                    href="/signup"
                    className="underline underline-offset-4"
                  >
                    Đăng ký
                  </a>             
                </div>
            </div>
          </form>
          <div className="bg-muted relative hidden md:block">
            <img
              src="/VIT-Banner-1.png"
              className="h-full w-full object-cover"
            />
          </div>
        </CardContent>
      </Card>
      <div className="text-sx text-balance px-6 text-center *:[a]:hover:text-primary text-muted-foreground *:[a]:underline *:[a]:underline-offetset-4">
        Bằng cách tiếp tục, bạn đồng ý với <a href="#">Điều khoản dịch vụ</a>{" "}
        và <a href="#">Chính sách bảo mật</a> của chúng tôi.
      </div>
    </div>
  )

}



